package for문;

public class ex01for문기초 {

	public static void main(String[] args) {

		// for문 구조
		// 1부터 10까지 출력
		for (int i = 0; i < 10; i++) {
			System.out.print(i + 1 + " ");
		}
		System.out.println(); // 개행 

		// 52부터 21까지 출력
		for (int i = 52; i > 20; i--) {
			System.out.print(i + " ");
		}

	}

}
