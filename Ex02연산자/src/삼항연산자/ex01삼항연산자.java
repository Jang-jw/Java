package 삼항연산자;

import java.util.Scanner;

public class ex01삼항연산자 {

	public static void main(String[] args) {

		// 삼항연산자 
		// 조건문 ? 실행문1 : 실행문2 
		// --> 조건문이 참이라면 실행문1 실행 
		// --> 조건문이 거짓이라면 실행문2 실행 
		
		int num1 = 4;
		int num2 = 7;
		
		String result = num1 < num2 ? "num2 더 크다." : "num1 더 크다.";
		System.out.println(result);

		 
		
		
		
		
		
	}

}
